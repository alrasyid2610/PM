let selectedRow = {
    id_testing_unit: null,
};

let table;

$(document).ready(function () {
    initDataTable(tableId);

    // when a row is clicked show detail
    $(tableId)
        .find("tbody")
        .on("click", "tr", function () {
            if ($(event.target).closest("button").length) return;
            const data = table.row(this).data();
            if (!data) return;
            selectedRow.id_testing_unit = data.id_testing_unit;
            loadDetail(data.id_testing_unit);
            $(tableId + " tr").removeClass("table-active");
            $(this).addClass("table-active");

            // show Detail tab
            const detailTab = new bootstrap.Tab(
                document.querySelector("#detail-tab"),
            );
            detailTab.show();
        });

    // handle delete (buttons generated by datatable render)
    $(tableId).on("click", ".btn-delete", function () {
        const id = $(this).data("id");
        Notify.confirm("Hapus data?", function () {
            $.ajax({
                url: window.route.update + id,
                method: "DELETE",
                data: { _token: window.route.csrf },
                success: function () {
                    Notify.success("Data berhasil dihapus");
                    table.ajax.reload();
                },
                error: function () {
                    Notify.error("Gagal menghapus data");
                },
            });
        });
    });
});

function loadDetail(id_testing_unit) {
    $("#detailContent").html("Loading...");

    $.get(window.route.update + id_testing_unit, function (res) {
        // build detail form using same fields as create
        const html = `
            <form class="row g-3" id="detailForm">

                <div class="col-md-12">
                    <div class="d-flex justify-content-between align-items-center mb-2">
                        <h3>Testing Unit</h3>
                        <div class="btn-group">
                            <button class="btn btn-warning btn-sm btn-edit-context" title="Edit Testing Unit">
                                <i class="fa-solid fa-pen"></i>
                            </button>
                        </div>
                    </div>

                    <div class="row mb-4">
                        <input type="hidden" name="_token" value="${window.route.csrf}">

                        <div class="col-md-12 col-lg-3 mb-3">
                            <label class="form-label required">Kode</label>
                            <input type="text" name="kode" class="form-control disabled" value="${res.kode}" required>
                        </div>

                        <div class="col-md-12 col-lg-4 mb-3">
                            <label class="form-label required">Judul Indonesia</label>
                            <input type="text" name="judul_indonesia" class="form-control disabled" value="${res.judul_indonesia}" required>
                        </div>

                        <div class="col-md-12 col-lg-4 mb-3">
                            <label class="form-label required">Judul Inggris</label>
                            <input type="text" name="judul_inggris" class="form-control disabled" value="${res.judul_inggris}" required>
                        </div>

                        <div class="col-md-12 mb-3">
                            <label class="form-label">Keterangan</label>
                            <textarea name="keterangan" class="form-control disabled" rows="3">${res.keterangan ?? ""}</textarea>
                        </div>
                    </div>

                </div>

            </form>
        `;

        $("#detailContent").html(html);

        // disable inputs initially
        $("#detailContent")
            .find("input, select, textarea")
            .prop("disabled", true);

        // edit/cancel toggle and save
        $(".btn-edit-context").on("click", function (e) {
            e.preventDefault();
            const $btn = $(this);
            const isEditing = $btn.hasClass("editing");

            if (!isEditing) {
                // switch to edit mode
                $("#detailContent")
                    .find("input, select, textarea")
                    .prop("disabled", false);
                $("#detailContent")
                    .find("input, select, textarea")
                    .removeClass("disabled");

                $btn.addClass("editing")
                    .removeClass("btn-warning")
                    .addClass("btn-secondary")
                    .html('<i class="fa-solid fa-times"></i>');

                // add save button
                $btn.after(`
                    <button class="btn btn-success btn-sm btn-save-context ms-2" title="Simpan">
                        <i class="fa-solid fa-check"></i>
                    </button>
                `);

                $(".btn-save-context").on("click", function (e) {
                    e.preventDefault();
                    submitForm();
                });
            } else {
                // cancel edit
                $("#detailContent")
                    .find("input, select, textarea")
                    .prop("disabled", true);
                $("#detailContent")
                    .find("input, select, textarea")
                    .addClass("disabled");

                $btn.removeClass("editing")
                    .addClass("btn-warning")
                    .removeClass("btn-secondary")
                    .html('<i class="fa-solid fa-pen"></i>');
                $(".btn-save-context").remove();
            }
        });
    }).fail(function () {
        $("#detailContent").html("Gagal memuat detail");
    });
}

function submitForm() {
    const formData = $("#detailForm").serialize();
    Notify.confirm("Simpan Data?", function () {
        $.ajax({
            url: window.route.update + selectedRow.id_testing_unit,
            method: "PUT",
            data: formData,
            success: function (response) {
                Notify.success("Data berhasil diperbarui");
                // if (typeof table !== "undefined") table.ajax.reload();
                loadDetail(selectedRow.id_testing_unit);
            },
            error: function (xhr) {
                Notify.error("Gagal memperbarui data");
            },
        });
    });
}
